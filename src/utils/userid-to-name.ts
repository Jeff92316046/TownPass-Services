/**
 * 根據輸入字串（如 UUID）穩定映射到中文動物名稱
 * @param {string} input - 任意字串或 UUID
 * @returns {string} - 對應的中文動物名稱
 */
export function uuidToAnimal(input: string): string {
  const hash = sha256(input);
  const num = Number.parseInt(hash.slice(0, 8), 16);
  return animals[num % animals.length];
}